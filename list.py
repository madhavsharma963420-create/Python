# Simple list of numbers
lst = [10, 20, 30, 40]

# Print list
print("List:", lst)

# Add element
lst.append(50)
print("After adding:", lst)

# Remove element
lst.remove(20)
print("After removing:", lst)

# Access element
print("First element:", lst[0])

# Loop through list
print("All elements:")
for i in lst:
    print(i)